"""

Numero divisivel por 3 e por 5

"""

n1 = int(input('Digite um numero: '))

if n1 % 3 == 0:
    print('O numero é divisivel por 3.')
else:
    print('O numero não é divisivel por 3. ')
print('---------------------------------------------------')
if n1 % 5 == 0:
    print('O numero é divisivel por 5.')
else:
    print('O numero não é divisivel por 5. ')
